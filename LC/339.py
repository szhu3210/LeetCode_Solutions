class Solution(object):
    def depthSum(self, nestedList):
        """
        :type nestedList: List[NestedInteger]
        :rtype: int
        """
        level=0
        res=0
        l=nestedList
        while l:
            level+=1
            nl=[]
            for x in l:
                if x.isInteger():
                    res+=level*x.getInteger()
                else:
                    nl.extend(x.getList())
            l=nl
        return res